var activeTab = 'all';
var searchQ   = '';

function esc(s) {
  return String(s).replace(/[<>&"']/g, function(c) {
    return { '<':'&lt;', '>':'&gt;', '&':'&amp;', '"':'&quot;', "'":'&#39;' }[c];
  });
}


function accHTML(item, id) {
  var tip = item.tip
    ? '<div style="display:flex;align-items:flex-start;gap:.5rem;margin-top:.75rem;padding:.75rem 1rem;background:var(--teal-bg);border-radius:8px;border-left:3px solid var(--teal);font-size:.8125rem;color:var(--teal-dark);line-height:1.6;">' +
        '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" style="width:14px;height:14px;flex-shrink:0;margin-top:1px;"><circle cx="12" cy="12" r="10"/><line x1="12" y1="8" x2="12" y2="12"/><line x1="12" y1="16" x2="12.01" y2="16"/></svg>' +
        item.tip + '</div>'
    : '';
  return '<div class="acc-item">' +
    '<button class="acc-btn" data-id="' + id + '">' +
      '<span class="acc-q">' + item.q + '</span>' +
      '<span class="acc-plus">' +
        '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" style="width:10px;height:10px;">' +
          '<line x1="12" y1="5" x2="12" y2="19"/><line x1="5" y1="12" x2="19" y2="12"/>' +
        '</svg>' +
      '</span>' +
    '</button>' +
    '<div class="acc-body" data-id="' + id + '"><p>' + item.a + tip + '</p></div>' +
  '</div>';
}

function colHTML(title, icon, itemsHTML) {
  var head = title
    ? '<div class="faq-col-head">' + icon + '<span>' + esc(title) + '</span></div>'
    : '<div style="height:30px;"></div>';
  return '<div>' + head + '<div class="acc">' + itemsHTML.join('') + '</div></div>';
}


function renderFAQ() {
  var grid = document.getElementById('faqGrid');
  if (!grid) return;

  var cats = Object.keys(KB);

  if (searchQ) {
    var q = searchQ.toLowerCase();
    var hits = [];
    cats.forEach(function(k) {
      KB[k].items.forEach(function(item) {
        if (item.q.toLowerCase().includes(q) || item.a.toLowerCase().includes(q)) {
          hits.push({ cat: k, item: item });
        }
      });
    });

    if (!hits.length) {
      grid.innerHTML =
        '<div class="no-results">' +
          '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5">' +
            '<circle cx="11" cy="11" r="8"/><line x1="21" y1="21" x2="16.65" y2="16.65"/>' +
          '</svg>' +
          '<h3>No results for \u201c' + esc(searchQ) + '\u201d</h3>' +
          '<p>Try a simpler word, or pick a category above.</p>' +
        '</div>';
      return;
    }

    var mid = Math.ceil(hits.length / 2);
    var searchIcon = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" style="width:17px;height:17px;"><circle cx="11" cy="11" r="8"/><line x1="21" y1="21" x2="16.65" y2="16.65"/></svg>';
    grid.innerHTML =
      colHTML('Search Results', searchIcon, hits.slice(0, mid).map(function(e, i) { return accHTML(e.item, 'SL' + i); })) +
      colHTML('', '', hits.slice(mid).map(function(e, i) { return accHTML(e.item, 'SR' + i); }));
    bindAcc();
    return;
  }

 
  if (activeTab !== 'all') {
    var sec  = KB[activeTab];
    if (!sec) return;
    var itms = sec.items;
    var mid  = Math.ceil(itms.length / 2);
    grid.innerHTML =
      colHTML(sec.title, sec.icon, itms.slice(0, mid).map(function(item, i) { return accHTML(item, activeTab + 'L' + i); })) +
      '<div>' +
        '<div class="faq-col-head" style="opacity:0;">' + sec.icon + '<span>' + sec.title + '</span></div>' +
        '<div class="acc">' + itms.slice(mid).map(function(item, i) { return accHTML(item, activeTab + 'R' + i); }).join('') + '</div>' +
      '</div>';
    bindAcc();
    return;
  }


  var left = '', right = '';
  cats.forEach(function(k, idx) {
    var sec  = KB[k];
    var html = '<div style="margin-bottom:2.5rem;">' +
      '<div class="faq-col-head">' + sec.icon + '<span>' + sec.title + '</span></div>' +
      '<div class="acc">' + sec.items.map(function(item, i) { return accHTML(item, k + i); }).join('') + '</div>' +
    '</div>';
    if (idx % 2 === 0) left  += html;
    else               right += html;
  });
  grid.innerHTML = '<div>' + left + '</div><div>' + right + '</div>';
  bindAcc();
}


function bindAcc() {
  document.querySelectorAll('.acc-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      var id   = this.dataset.id;
      var body = document.querySelector('.acc-body[data-id="' + id + '"]');
      var was  = this.classList.contains('open');

      document.querySelectorAll('.acc-btn').forEach(function(b) { b.classList.remove('open'); });
      document.querySelectorAll('.acc-body').forEach(function(b) { b.classList.remove('open'); });

      if (!was) {
        this.classList.add('open');
        if (body) body.classList.add('open');
      }
    });
  });
}


function initTabs() {
  var tabsEl = document.getElementById('faqTabs');
  if (!tabsEl) return;
  tabsEl.addEventListener('click', function(e) {
    var btn = e.target.closest('.ftab');
    if (!btn) return;
    document.querySelectorAll('.ftab').forEach(function(b) { b.classList.remove('active'); });
    btn.classList.add('active');
    activeTab = btn.dataset.cat;
    searchQ   = '';
    var si = document.getElementById('searchInput');
    if (si) si.value = '';
    renderFAQ();
    var faq = document.getElementById('faq');
    if (faq) faq.scrollIntoView({ behavior: 'smooth', block: 'start' });
  });
}

function initSearch() {
  var input = document.getElementById('searchInput');
  if (!input) return;
  var timer;
  input.addEventListener('input', function() {
    clearTimeout(timer);
    var val = this.value.trim();
    timer = setTimeout(function() {
      searchQ = val;
      if (val) {
        document.querySelectorAll('.ftab').forEach(function(b) { b.classList.remove('active'); });
      } else {
        var active = document.querySelector('[data-cat="' + activeTab + '"]');
        if (active) active.classList.add('active');
      }
      renderFAQ();
      if (val) {
        var faq = document.getElementById('faq');
        if (faq) faq.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    }, 280);
  });
}

window.quickSearch = function(term) {
  var input = document.getElementById('searchInput');
  if (input) input.value = term;
  searchQ = term;
  document.querySelectorAll('.ftab').forEach(function(b) { b.classList.remove('active'); });
  renderFAQ();
  var faq = document.getElementById('faq');
  if (faq) faq.scrollIntoView({ behavior: 'smooth', block: 'start' });
  return false;
};


window.setTab = function(cat) {
  searchQ   = '';
  activeTab = cat;
  var input = document.getElementById('searchInput');
  if (input) input.value = '';
  document.querySelectorAll('.ftab').forEach(function(b) { b.classList.remove('active'); });
  var tab = document.querySelector('[data-cat="' + cat + '"]');
  if (tab) tab.classList.add('active');
  renderFAQ();
  var faq = document.getElementById('faq');
  if (faq) faq.scrollIntoView({ behavior: 'smooth', block: 'start' });
};


function initContactForm() {
  var form = document.getElementById('contactForm');
  if (!form) return;

 
  var msg     = document.getElementById('cMsg');
  var counter = document.getElementById('charCount');
  if (msg && counter) {
    msg.addEventListener('input', function() { counter.textContent = this.value.length; });
  }

  form.addEventListener('submit', function(e) {
    e.preventDefault();

    var name    = (document.getElementById('cName')    || {}).value || '';
    var email   = (document.getElementById('cEmail')   || {}).value || '';
    var topic   = (document.getElementById('cTopic')   || {}).value || '';
    var subject = (document.getElementById('cSubject') || {}).value || '';
    var message = (document.getElementById('cMsg')     || {}).value || '';
    var btn     = document.getElementById('sendBtn');

  
    if (!name.trim()) { window.showToast('Please enter your full name.'); return; }
    if (!window.validateEmail(email)) { window.showToast('Please enter a valid email address.'); return; }
    if (!topic)        { window.showToast('Please select a topic.'); return; }
    if (!subject.trim()){ window.showToast('Please add a subject line.'); return; }
    if (message.trim().length < 10) { window.showToast('Please describe your issue in a bit more detail.'); return; }

  
    if (btn) {
      btn.innerHTML = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" style="width:16px;height:16px;"><polyline points="20,6 9,17 4,12"/></svg> Sent!';
      btn.style.background = '#16A34A';
      btn.disabled = true;
    }
    window.showToast('Message received! We\u2019ll reply within 2\u20134 hours.');

    setTimeout(function() {
      form.reset();
      if (counter) counter.textContent = '0';
      if (btn) {
        btn.innerHTML = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" style="width:16px;height:16px;"><line x1="22" y1="2" x2="11" y2="13"/><polygon points="22,2 15,22 11,13 2,9"/></svg> Send Message';
        btn.style.background = '';
        btn.disabled = false;
      }
    }, 4000);
  });
}


document.addEventListener('DOMContentLoaded', function() {
  initTabs();
  initSearch();
  initContactForm();
  renderFAQ();
});